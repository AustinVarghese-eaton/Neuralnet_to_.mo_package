def _compile_dll(
    model_c_src: str,
    pkg_name: str,
    gcc_exe: str,
    out_dir: Path,
) -> Path | None:
    """
    Compile model.c -> <pkg_name>.dll using the provided gcc executable.
    Returns path to the .dll on success, None on failure.
    """
    import os

    with tempfile.TemporaryDirectory() as tmp:
        tmp_path = Path(tmp)
        c_file = tmp_path / "model.c"
        dll_out = tmp_path / f"{pkg_name}.dll"
        c_file.write_text(model_c_src, encoding="utf-8")

        cmd = [
            gcc_exe,
            "-shared",
            "-fPIC",
            "-O2",
            "-DFMI2_FUNCTION_PREFIX=",
            "-o", str(dll_out),
            str(c_file),
        ]

        # Ensure gcc's own bin directory is on PATH so it can locate cc1/as/ld
        env = os.environ.copy()
        gcc_bin_dir = str(Path(gcc_exe).parent)
        existing_path = env.get("PATH", "")
        if gcc_bin_dir.lower() not in existing_path.lower():
            env["PATH"] = gcc_bin_dir + os.pathsep + existing_path

        log.info("FMU: compiling DLL: %s", " ".join(cmd))
        try:
            result = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                timeout=120,
                env=env,
            )
        except FileNotFoundError:
            log.error("FMU: gcc not found at '%s'", gcc_exe)
            return None
        except subprocess.TimeoutExpired:
            log.error("FMU: gcc compilation timed out.")
            return None

        if result.returncode != 0:
            log.error("FMU: gcc compilation failed (rc=%d):\n%s\n%s",
                      result.returncode, result.stdout, result.stderr)
            return None

        if not dll_out.exists():
            log.error("FMU: gcc reported success but DLL not found at %s", dll_out)
            return None

        out_dir.mkdir(parents=True, exist_ok=True)
        dest = out_dir / f"{pkg_name}.dll"
        shutil.copy2(dll_out, dest)
        log.info("FMU: DLL compiled successfully → %s", dest)
        return dest


