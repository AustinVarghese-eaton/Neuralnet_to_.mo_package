def create_fmu(run_id: str, attempt_num: int) -> dict:
    """
    Stage 5b: Generate FMU 2.0 Model Exchange from mlp_weights.json.

    Output:
        runs/<run_id>/attempts/attempt_XXX/fmu/<PackageName>.fmu
          ├── modelDescription.xml
          ├── sources/model.c
          └── binaries/win64/<PackageName>.dll   (if gcc found)
    """
    run_dir = runs_root() / run_id
    cfg_path = run_dir / "input" / "run_config.json"
    if not cfg_path.exists():
        raise FileNotFoundError(f"run_config.json not found: {cfg_path}")

    cfg = load_run_config(cfg_path)
    status_path = run_dir / "status.json"

    write_status(status_path, state="CREATE_FMU", message="Creating FMU 2.0 Model Exchange...", progress=0.75)

    attempt_dir = run_dir / "attempts" / f"attempt_{attempt_num:03d}"
    weights_path = attempt_dir / "processed" / "mlp_weights.json"
    if not weights_path.exists():
        raise FileNotFoundError(f"mlp_weights.json not found: {weights_path}. Run training first.")

    export = json.loads(weights_path.read_text(encoding="utf-8"))

    pkg_name = cfg.package_name
    input_cols = cfg.input_columns
    output_cols = cfg.output_columns

    fmu_dir = attempt_dir / "fmu"
    fmu_dir.mkdir(parents=True, exist_ok=True)

    # --- Generate artifacts ---
    write_status(status_path, state="CREATE_FMU", message="Generating modelDescription.xml...", progress=0.77)
    run_guid = str(uuid.uuid4())
    xml = _gen_model_description_xml(pkg_name, input_cols, output_cols, run_guid)

    write_status(status_path, state="CREATE_FMU", message="Generating model.c (MLP forward pass)...", progress=0.79)
    model_c = _gen_model_c(pkg_name, input_cols, output_cols, export)

    # --- Compile DLL ---
    gcc_exe = _resolve_gcc(getattr(cfg, "gcc_path", None))
    dll_path: Path | None = None

    if gcc_exe:
        write_status(status_path, state="CREATE_FMU", message="Compiling Windows DLL via gcc...", progress=0.81)
        dll_path = _compile_dll(model_c, pkg_name, gcc_exe, fmu_dir / "binaries" / "win64")
        if dll_path is None:
            msg = (
                "FMU DLL compilation failed. "
                "FMU will be source-only — OpenModelica may need to compile it on import. "
                "Check pipeline.log for gcc error details."
            )
            log.warning(msg)
            write_status(status_path, state="CREATE_FMU", message=msg, progress=0.81)
    else:
        msg = (
            "gcc not found. FMU will be source-only (no binaries/win64/ DLL). "
            "To enable pre-compiled binary, install OpenModelica — gcc is auto-detected from: "
            r"C:\OpenModelica*\tools\msys\ucrt64\bin\gcc.exe (OM 1.24+) "
            r"or ~\OneDrive*\Documents\OpenModelica*\tools\msys\ucrt64\bin\gcc.exe. "
            "Alternatively set gcc_path in RunConfig."
        )
        log.warning(msg)
        write_status(status_path, state="CREATE_FMU", message=msg, progress=0.81)

    # --- Pack .fmu ---
    write_status(status_path, state="CREATE_FMU", message="Packing .fmu archive...", progress=0.82)
    fmu_path = fmu_dir / f"{pkg_name}.fmu"
    _pack_fmu(fmu_path, xml, model_c, dll_path, pkg_name)

    has_binary = dll_path is not None and dll_path.exists()
    write_status(
        status_path,
        state="CREATE_FMU_DONE",
        message=f"FMU created: {fmu_path.name} ({'with DLL' if has_binary else 'source-only'})",
        progress=0.83,
    )

    return {
        "timestamp_utc": _utc_now(),
        "run_id": run_id,
        "attempt": f"attempt_{attempt_num:03d}",
        "package_name": pkg_name,
        "fmu_path": str(fmu_path),
        "has_binary": has_binary,
        "gcc_used": gcc_exe,
    }
