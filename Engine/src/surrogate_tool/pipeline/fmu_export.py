def _pack_fmu(
    fmu_path: Path,
    xml: str,
    model_c: str,
    dll_path: Path | None,
    pkg_name: str,
) -> None:
    fmu_path.parent.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(fmu_path, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("modelDescription.xml", xml.encode("utf-8"))
        zf.writestr(f"sources/model.c", model_c.encode("utf-8"))
        if dll_path and dll_path.exists():
            zf.write(dll_path, f"binaries/win64/{pkg_name}.dll")
    log.info("FMU: packed → %s", fmu_path)


