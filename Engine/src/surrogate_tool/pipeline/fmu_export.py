def _gen_model_description_xml(
    pkg_name: str,
    input_cols: list[str],
    output_cols: list[str],
    guid: str,
) -> str:
    n_in = len(input_cols)
    n_out = len(output_cols)

    sv_lines: list[str] = []
    # inputs: vr 0 .. n_in-1
    # FMI 2.0: continuous inputs must NOT carry initial="exact"; <Real> requires start attribute
    for i, col in enumerate(input_cols):
        sv_lines.append(
            f'    <ScalarVariable name="{col}" valueReference="{i}" '
            f'causality="input" variability="continuous">'
            f'\n      <Real start="0.0"/>\n    </ScalarVariable>'
        )
    # outputs: vr n_in .. n_in+n_out-1
    dep_str = " ".join(str(i + 1) for i in range(n_in))  # 1-based indices per FMI 2.0 spec
    for j, col in enumerate(output_cols):
        vr = n_in + j
        sv_lines.append(
            f'    <ScalarVariable name="{col}" valueReference="{vr}" '
            f'causality="output" variability="continuous" initial="calculated">'
            f'\n      <Real/>\n    </ScalarVariable>'
        )

    unknowns: list[str] = []
    for k in range(1, n_out + 1):
        idx = n_in + k  # 1-based index in ScalarVariable list
        unknowns.append(
            f'      <Unknown index="{idx}" dependencies="{dep_str}"/>'
        )

    xml = f"""<?xml version="1.0" encoding="UTF-8"?>
<fmiModelDescription
  fmiVersion="2.0"
  modelName="{pkg_name}"
  guid="{{{guid}}}"
  description="MLP surrogate model generated by SurrogateGenerator"
  generationTool="SurrogateGenerator"
  generationDateAndTime="{_utc_now()}"
  variableNamingConvention="flat"
  numberOfEventIndicators="0">

  <ModelExchange modelIdentifier="{pkg_name}"/>

  <LogCategories>
    <Category name="logAll"/>
    <Category name="logStatusError"/>
  </LogCategories>

  <ModelVariables>
{chr(10).join(sv_lines)}
  </ModelVariables>

  <ModelStructure>
    <Outputs>
{chr(10).join(unknowns)}
    </Outputs>
  </ModelStructure>

</fmiModelDescription>
"""
    return xml


