def _utc_now() -> str:
    from datetime import datetime, timezone
    return datetime.now(timezone.utc).isoformat(timespec="seconds")


def _resolve_gcc(cfg_gcc_path: str | None) -> str | None:
    """
    Locate gcc in priority order:
      1. OpenModelica bundled MinGW  (C:/OpenModelica*/tools/msys/mingw64/bin/gcc.exe)
      2. System PATH  (shutil.which)
      3. cfg.gcc_path  (explicit user override)
    Returns the path string if found, None otherwise.
    """
    import glob

    # 1 — OpenModelica bundled gcc (search all installed versions and locations)
    # OM 1.24+ (ucrt64) and older (mingw64) are both searched.
    # Also covers OneDrive/Documents installs (non-standard location).
    home = Path.home()
    # Build root globs: standard C:\ locations + OneDrive variants + user Documents
    om_root_patterns = [
        r"C:\OpenModelica*",
        r"C:\Program Files\OpenModelica*",
        r"C:\Program Files (x86)\OpenModelica*",
        str(home / "Documents" / "OpenModelica*"),
    ]
    # Also add every OneDrive-style folder under the user home
    for od in home.glob("OneDrive*"):
        om_root_patterns.append(str(od / "Documents" / "OpenModelica*"))

    gcc_suffixes = [
        r"tools\msys\ucrt64\bin\gcc.exe",   # OM 1.24+ preferred
        r"tools\msys\mingw64\bin\gcc.exe",  # OM older
    ]

    om_candidates: list[str] = []
    for root_pat in om_root_patterns:
        for suffix in gcc_suffixes:
            om_candidates.extend(glob.glob(str(Path(root_pat) / suffix)))

    if om_candidates:
        # sort descending so latest version string is first
        om_candidates.sort(reverse=True)
        log.info("FMU: using OpenModelica bundled gcc: %s", om_candidates[0])
        return om_candidates[0]

    # 2 — system PATH
    path_gcc = shutil.which("gcc")
    if path_gcc:
        log.info("FMU: using system gcc from PATH: %s", path_gcc)
        return path_gcc

    # 3 — explicit override from RunConfig
    if cfg_gcc_path:
        p = Path(cfg_gcc_path)
        if p.exists():
            log.info("FMU: using user-configured gcc: %s", cfg_gcc_path)
            return str(p)
        else:
            log.warning("FMU: cfg.gcc_path set to '%s' but file not found.", cfg_gcc_path)

    return None


